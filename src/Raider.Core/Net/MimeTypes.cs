		public static IReadOnlyList<string> GetExtensions(string mimeType)
		{
			mimeType = mimeType.ToLower();
			if (_mimeTypeToExt.Value.TryGetValue(mimeType, out IReadOnlyList<string>? extensions))
				return extensions;

			return new List<string>();
		}

		public static IReadOnlyList<string> GetMimeTypes(string extension)
		{
			extension = extension.ToLower().TrimPrefix(".");
			if (_extToMimeType.Value.TryGetValue(extension, out IReadOnlyList<string>? mimeTypes))
				return mimeTypes;

			return new List<string>();
		}

		public static string? GetFirstExtension(string mimeType)
		{
			mimeType = mimeType.ToLower();
			if (_mimeTypeToExt.Value.TryGetValue(mimeType, out IReadOnlyList<string>? extensions))
				return extensions.FirstOrDefault();

			return null;
		}

		public static string? GetFirstMimeType(string extension)
